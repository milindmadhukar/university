class Math_Library
{
    public static void main(int q)
    {
        
        int x = 5 ;
        int y = 2 ;
        int z = 64 ;
        System.out.println("Min = " + Math.min(x,y) ) ;
        System.out.println("Max = " + Math.max(x,y) ) ;
        System.out.println("Pow = " + Math.pow(x,y) );
        System.out.println("Exp = " + Math.exp(x) ) ;
        System.out.println("Sqrt = " + Math.sqrt(z) ) ;
        System.out.println("Cbrt = " + Math.cbrt(z) ) ;
        double a = 2.34 ;
        System.out.println("Ceiling = " +Math.ceil(a)) ;
        System.out.println("Floor = " +Math.floor(a)) ;
        System.out.println("Round = " +Math.round(a)) ;
        System.out.println("Absolute = " +Math.abs(a)) ;
    }
}